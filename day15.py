import pandas as pd

# 1. データの準備（スターク・インダストリーズの在庫リスト）
data = {
    '商品名': ['アイアンマンスーツ', 'アークリアクター', 'シールド', 'ウェブシューター'],
    '価格': [500000, 100000, 30000, 5000],
    '在庫': [1, 5, 0, 10]
}
df = pd.DataFrame(data)

print("--- 全データ ---")
print(df)

# 2. 今日のミッション：特定の条件で「狙撃」抽出
# 例：価格が100,000円以上の行だけを抽出
expensive_items = df[df['価格'] >= 100000]

print("\n--- 10万円以上の高額商品 ---")
print(expensive_items)

# ここに「タスクA（在庫0）」や「タスクB（1万未満）」を追記してみてください！
# --- ここから下に書き足す ---

# タスクA：在庫が「0」の商品（欠品）を抽出
out_of_stock = df[df['在庫'] == 0]

print("\n--- 在庫切れの商品 ---")
print(out_of_stock)

# タスクB：価格が「10,000円未満」の商品を抽出
cheap_items = df[df['価格'] < 10000]

print("\n--- 1万円未満のお手頃商品 ---")
print(cheap_items)